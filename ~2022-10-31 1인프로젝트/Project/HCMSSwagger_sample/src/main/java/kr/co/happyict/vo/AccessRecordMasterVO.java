package kr.co.happyict.vo;

import java.io.Serializable;
import kr.co.happyict.util.CommonUtil;

/**
 * 접속 기록(Mic system에서 이용)
 *
 * @author vegaaltair77@happyict.co.kr
 */
public class AccessRecordMasterVO extends PageVO implements Serializable {

  private static final long serialVersionUID = -1254514332406461415L;

  private String SYSTEMID;
  private String USERID;
  private String USERNAME;
  private String IPADDRESS;
  private String MACADDRESS;
  private String PERSONALINFORMATIONYN;
  private String ACTIONTEXT;
  private String MENUID;
  private String MENUNAME;
  private String POLINGDATETIME;

  private int ACTION;

  public String getSYSTEMID() {
    return CommonUtil.checkString(SYSTEMID);
  }

  public void setSYSTEMID(String SYSTEMID) {
    this.SYSTEMID = SYSTEMID;
  }

  public String getUSERID() {
    return CommonUtil.checkString(USERID);
  }

  public void setUSERID(String USERID) {
    this.USERID = USERID;
  }

  public String getUSERNAME() {
    return CommonUtil.checkString(USERNAME);
  }

  public void setUSERNAME(String USERNAME) {
    this.USERNAME = USERNAME;
  }

  public String getIPADDRESS() {
    return CommonUtil.checkString(IPADDRESS);
  }

  public void setIPADDRESS(String IPADDRESS) {
    this.IPADDRESS = IPADDRESS;
  }

  public String getMACADDRESS() {
    return CommonUtil.checkString(MACADDRESS);
  }

  public void setMACADDRESS(String MACADDRESS) {
    this.MACADDRESS = MACADDRESS;
  }

  public String getPERSONALINFORMATIONYN() {
    return CommonUtil.checkString(PERSONALINFORMATIONYN);
  }

  public void setPERSONALINFORMATIONYN(String PERSONALINFORMATIONYN) {
    this.PERSONALINFORMATIONYN = PERSONALINFORMATIONYN;
  }

  public String getACTIONTEXT() {
    return CommonUtil.checkString(ACTIONTEXT);
  }

  public void setACTIONTEXT(String ACTIONTEXT) {
    this.ACTIONTEXT = ACTIONTEXT;
  }

  public String getMENUID() {
    return CommonUtil.checkString(MENUID);
  }

  public void setMENUID(String MENUID) {
    this.MENUID = MENUID;
  }

  public String getMENUNAME() {
    return CommonUtil.checkString(MENUNAME);
  }

  public void setMENUNAME(String MENUNAME) {
    this.MENUNAME = MENUNAME;
  }

  public String getPOLINGDATETIME() {
    return CommonUtil.checkString(POLINGDATETIME);
  }

  public void setPOLINGDATETIME(String POLINGDATETIME) {
    this.POLINGDATETIME = POLINGDATETIME;
  }

  public int getACTION() {
    return CommonUtil.parseInt(ACTION + "");
  }

  public void setACTION(int ACTION) {
    this.ACTION = ACTION;
  }
}
